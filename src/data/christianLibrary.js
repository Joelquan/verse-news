export function filterLibrary({ era = 'all', category = 'all', query = '' } = {}) {
  const q = query.trim().toLowerCase()
  return CHRISTIAN_LIBRARY.filter((b) => {
    if (era !== 'all' && b.era !== era) return false
    if (category !== 'all' && b.category !== category) return false
    if (!q) return true
    const blob = [b.title, b.author, b.description, b.century, b.year, b.license]
      .filter(Boolean)
      .join(' ')
      .toLowerCase()
    return blob.includes(q)
  })
}
