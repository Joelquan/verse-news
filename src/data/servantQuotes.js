const FIFTEEN_MIN_MS = 15 * 60 * 1000

/**
 * Quote that stays fixed for the current 15-minute window.
 * Same quote for all visitors in the same window.
 */
export function getQuoteOfTheMoment(now = new Date()) {
  const slot = Math.floor(now.getTime() / FIFTEEN_MIN_MS)
  const quote = SERVANT_QUOTES[Math.abs(slot) % SERVANT_QUOTES.length]
  const windowStart = slot * FIFTEEN_MIN_MS
  const windowEnd = windowStart + FIFTEEN_MIN_MS
  return {
    ...quote,
    slot,
    windowStart: new Date(windowStart),
    windowEnd: new Date(windowEnd),
    nextChangeAt: new Date(windowEnd),
  }
}

/** Human label for the current 15-minute block, local time */
export function formatQuoteWindow(quote, now = new Date()) {
  if (!quote?.windowStart || !quote?.windowEnd) return "Refreshes every 15 minutes"
  const opts = { hour: "numeric", minute: "2-digit" }
  const start = quote.windowStart.toLocaleTimeString(undefined, opts)
  const end = quote.windowEnd.toLocaleTimeString(undefined, opts)
  const msLeft = Math.max(0, quote.windowEnd.getTime() - now.getTime())
  const mins = Math.floor(msLeft / 60000)
  const secs = Math.floor((msLeft % 60000) / 1000)
  const left = mins > 0 ? `${mins}m ${secs}s` : `${secs}s`
  return `${start} – ${end} · changes in ${left}`
}
